public class Processes_method {
    double result=0;

    double sum(double number_one,double number_tow){
        return result=number_one+number_tow;
    }//end method sum ()

    double minus(double number_one,double number_tow){
        return result=number_one-number_tow;
    }// end method minus ()


    double dividing(double number_one,double number_tow){
        return result=number_one/number_tow;
    }// end method dividing ()


    double multi(double number_one,double number_tow){
        return result=number_one*number_tow;
    }// end method multi ()

}// end class Processes_method
